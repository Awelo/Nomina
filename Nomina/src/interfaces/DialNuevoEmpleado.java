    private void cargos(){
        Control controla= new Control();
        ResultSet obtener= controla.darCargos();
        cmbxCargo.addItem("--Selecciona--");
        try{
            while(obtener.next()){
                cmbxCargo.addItem(obtener.getString(1));
            }
        }catch(SQLException e){
            System.out.println("Error en los datos "+e);
        }
    }

    private boolean validar(){
        boolean pasa=true;
        if(txtNombre.getText().equals("") || txtApepat.getText().equals("") || txtTelefono.getText().equals("") || txtEmail.getText().equals("") || txtCalle.getText().equals("") || txtCP.getText().equals("") || txtColonia.getText().equals("") || txtCiudad.getText().equals("")){
            pasa=false;
        }else if(cmbxCargo.getSelectedIndex()==0){
            pasa=false;
        }
        return pasa;
    }
