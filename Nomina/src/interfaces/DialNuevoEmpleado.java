    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(validar()){
            Empleado emplear = new Empleado();
            emplear.setNombre(txtNombre.getText());
            emplear.setApepat(txtApepat.getText());
            emplear.setApemat(txtApemat.getText());
            emplear.setCargo(cmbxCargo.getSelectedIndex());
            emplear.setTelefono(txtTelefono.getText());
            emplear.setEmail(txtEmail.getText());
            
            Domicilio domi = new Domicilio();
            domi.setCalle(txtCalle.getText());
            if(!txtNumExt.getText().equals("")){
                domi.setNumExt(txtNumExt.getText());
            }
            if(!txtNumInt.getText().equals("")){
                domi.setNumInt(txtNumInt.getText());
            }
            domi.setCp(txtCP.getText());
            domi.setColonia(txtColonia.getText());
            domi.setCiudad(txtCiudad.getText());
            
            Control contra = new Control();
            if(contra.addEmpleado(emplear, domi)==1){
                ConfirmLogout fijar= new ConfirmLogout((Frame) this.getParent(), true);
                fijar.setVisible(true);
                fijar.setLocationRelativeTo(null);
                int user=contra.setPassword(fijar.getPass());
                
                if(user>0){
                    JOptionPane.showMessageDialog(this, "Tu numero de usuario es "+user);
                }
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

